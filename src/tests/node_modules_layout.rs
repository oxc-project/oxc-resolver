use std::path::PathBuf;

use crate::{NodeModulesLayout, ResolveOptions, Resolver};

use super::fixture_root;

fn install_path(slug: &str) -> PathBuf {
    fixture_root().join("bench-pm").join("installs").join(slug)
}

fn is_installed(slug: &str) -> bool {
    let path = install_path(slug);
    if slug == "yarn-pnp" {
        path.join(".pnp.cjs").is_file()
    } else {
        path.join("node_modules").is_dir()
    }
}

fn assert_detected(slug: &str, expected: NodeModulesLayout) {
    if !is_installed(slug) {
        return;
    }
    let path = install_path(slug);
    let resolver =
        Resolver::new(ResolveOptions { cwd: Some(path.clone()), ..ResolveOptions::default() });
    assert_eq!(resolver.node_modules_layout(&path), expected, "{slug}");
}

#[test]
fn detect_npm_flat() {
    assert_detected("npm-flat", NodeModulesLayout::Flat);
}

#[test]
fn detect_pnpm_hoisted() {
    assert_detected("pnpm-hoisted", NodeModulesLayout::Isolated);
}

#[test]
fn detect_yarn_flat() {
    assert_detected("yarn-flat", NodeModulesLayout::Flat);
}

#[test]
fn detect_bun_flat() {
    assert_detected("bun-flat", NodeModulesLayout::Flat);
}

#[test]
fn detect_pnpm_isolated() {
    assert_detected("pnpm-isolated", NodeModulesLayout::Isolated);
}

#[test]
fn detect_yarn_isolated() {
    assert_detected("yarn-isolated", NodeModulesLayout::Isolated);
}

#[test]
fn detect_bun_isolated() {
    assert_detected("bun-isolated", NodeModulesLayout::Isolated);
}

#[cfg(feature = "yarn_pnp")]
#[test]
fn detect_yarn_pnp() {
    assert_detected("yarn-pnp", NodeModulesLayout::Pnp);
}

#[test]
fn explicit_layout_bypasses_detection() {
    let resolver = Resolver::new(ResolveOptions {
        node_modules_layout: NodeModulesLayout::Generic,
        ..ResolveOptions::default()
    });
    let path = install_path("npm-flat");
    assert_eq!(resolver.node_modules_layout(&path), NodeModulesLayout::Generic);
}

#[test]
fn explicit_flat_returned_without_filesystem_probe() {
    let resolver = Resolver::new(ResolveOptions {
        node_modules_layout: NodeModulesLayout::Flat,
        ..ResolveOptions::default()
    });
    assert_eq!(
        resolver.node_modules_layout(PathBuf::from("/definitely/does/not/exist")),
        NodeModulesLayout::Flat
    );
}

#[test]
fn detection_caches_first_result() {
    if !is_installed("npm-flat") {
        return;
    }
    let resolver = Resolver::new(ResolveOptions::default());
    let first = install_path("npm-flat");
    let second = install_path("pnpm-isolated");
    let detected_first = resolver.node_modules_layout(&first);
    let detected_second = resolver.node_modules_layout(&second);
    assert_eq!(detected_first, detected_second);
}
